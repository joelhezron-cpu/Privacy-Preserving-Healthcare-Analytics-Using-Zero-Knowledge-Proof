function saveToMasterRecords(healthData)
% Save health data to master records file
masterFile = 'master_health_records.mat';

if exist(masterFile, 'file')
    data = load(masterFile);
    if isfield(data, 'masterRecords')
        masterRecords = data.masterRecords;
        % Check if this is the first record or need to append
        if isstruct(masterRecords) && isfield(masterRecords, 'PatientName')
            % Convert to cell array if single struct
            masterRecords = {masterRecords};
        end
        masterRecords{end+1} = healthData;
    else
        masterRecords = {healthData};
    end
else
    masterRecords = {healthData};
end

save(masterFile, 'masterRecords');
end