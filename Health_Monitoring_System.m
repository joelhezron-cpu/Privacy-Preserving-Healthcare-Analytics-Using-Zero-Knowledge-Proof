% Nested callback functions
    function clearFields(~, ~)
        set(bpField, 'String', '');
        set(sugarField, 'String', '');
        set(hrField, 'String', '');
        set(spo2Field, 'String', '');
        set(cholField, 'String', '');
        set(tempField, 'String', '');
    end

    function saveHealthData(~, ~)
        % Get values from fields
        bp = strtrim(get(bpField, 'String'));
        sugar = strtrim(get(sugarField, 'String'));
        hr = strtrim(get(hrField, 'String'));
        spo2 = strtrim(get(spo2Field, 'String'));
        chol = strtrim(get(cholField, 'String'));
        temp = strtrim(get(tempField, 'String'));
        
        % Validation
        if isempty(bp)
            errordlg('Please enter Blood Pressure.', 'Validation Error');
            uicontrol(bpField);
            return;
        end
        
        if isempty(sugar)
            errordlg('Please enter Blood Sugar level.', 'Validation Error');
            uicontrol(sugarField);
            return;
        end
        
        if isempty(hr)
            errordlg('Please enter Heart Rate.', 'Validation Error');
            uicontrol(hrField);
            return;
        end
        
        if isempty(spo2)
            errordlg('Please enter SPO2 level.', 'Validation Error');
            uicontrol(spo2Field);
            return;
        end
        
        if isempty(chol)
            errordlg('Please enter Cholesterol level.', 'Validation Error');
            uicontrol(cholField);
            return;
        end
        
        if isempty(temp)
            errordlg('Please enter Temperature.', 'Validation Error');
            uicontrol(tempField);
            return;
        end
        
        % Numeric validation
        try
            % Check BP format
            if ~contains(bp, '/')
                error('Blood Pressure must be in format: systolic/diastolic (e.g., 120/80)');
            end
            
            % Convert to numeric
            sugarNum = str2double(sugar);
            hrNum = str2double(hr);
            spo2Num = str2double(spo2);
            cholNum = str2double(chol);
            tempNum = str2double(temp);
            
            % Validate ranges
            if isnan(sugarNum) || sugarNum <= 0 || sugarNum > 500
                error('Blood Sugar must be between 1-500 mg/dL');
            end
            
            if isnan(hrNum) || hrNum <= 0 || hrNum > 250
                error('Heart Rate must be between 1-250 bpm');
            end
            
            if isnan(spo2Num) || spo2Num < 0 || spo2Num > 100
                error('SPO2 must be between 0-100%');
            end
            
            if isnan(cholNum) || cholNum <= 0 || cholNum > 500
                error('Cholesterol must be between 1-500 mg/dL');
            end
            
            if isnan(tempNum) || tempNum < 30 || tempNum > 45
                error('Temperature must be between 30-45°C');
            end
            
        catch ME
            errordlg(ME.message, 'Validation Error');
            return;
        end
        
        % Create health data structure
        healthData = struct();
        healthData.PatientName = userName;
        healthData.Mobile = userMobile;
        healthData.BloodPressure = bp;
        healthData.BloodSugar = sugarNum;
        healthData.HeartRate = hrNum;
        healthData.SPO2 = spo2Num;
        healthData.Cholesterol = cholNum;
        healthData.Temperature = tempNum;
        healthData.RecordDate = datestr(now, 'dd-mm-yyyy HH:MM:SS');
        
        % Create safe filename
        safeName = strrep(userName, ' ', '_');
        safeName = regexprep(safeName, '[^a-zA-Z0-9_]', '');
        filename = sprintf('HealthData_%s_%s.mat', safeName, userMobile);
        
        % Save individual file
        save(filename, 'healthData');
        filename = sprintf('HealthData.mat');
        save(filename, 'healthData');
        % Save to master records
        saveToMasterRecords(healthData);
        
        % Show success message
        msgbox(sprintf('Health data saved successfully!\n\nPatient: %s\nFile: %s', userName, filename), ...
            'Success', 'modal');
        run('SDD.m');
        % Clear fields
        clearFields();
    end
end

