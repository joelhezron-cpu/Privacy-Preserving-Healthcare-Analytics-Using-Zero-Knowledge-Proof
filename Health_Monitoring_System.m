% Nested callback functions
    function togglePasswordVisibility(~, ~)
        if get(showPassCheck, 'Value')
            set(passwordField, 'Enable', 'on');
        else
            set(passwordField, 'Enable', 'inactive');
        end
    end

    function handlePasswordKey(src, evt, fig)
        storedPass = getappdata(fig, 'passwordStore');
        if isempty(storedPass)
            storedPass = '';
        end
        
        switch evt.Key
            case 'backspace'
                if ~isempty(storedPass)
                    storedPass = storedPass(1:end-1);
                end
            case 'delete'
                storedPass = '';
            otherwise
                if ~isempty(evt.Character) && ~any(strcmp(evt.Key, {'backspace', 'delete', 'return', 'escape'}))
                    storedPass = [storedPass evt.Character];
                end
        end
        
        setappdata(fig, 'passwordStore', storedPass);
        set(src, 'String', repmat('*', 1, length(storedPass)));
    end

    function clearFields(~, ~)
        set(nameField, 'String', '');
        set(mobileField, 'String', '');
        set(passwordField, 'String', '');
        setappdata(regFig, 'passwordStore', '');
        set(showPassCheck, 'Value', 0);
        togglePasswordVisibility();
    end

    function registerPatient(~, ~)
        name = strtrim(get(nameField, 'String'));
        mobile = strtrim(get(mobileField, 'String'));
        password = getappdata(regFig, 'passwordStore');
        
        % Validation
        if isempty(name)
            errordlg('Please enter patient name.', 'Validation Error');
            return;
        end
        
        if isempty(mobile) || ~all(isdigit(mobile)) || length(mobile) ~= 10
            errordlg('Please enter a valid 10-digit mobile number.', 'Validation Error');
            return;
        end
        
        if isempty(password) || length(password) < 4
            errordlg('Password must be at least 4 characters long.', 'Validation Error');
            return;
        end
        
        % Load existing users
        users = loadUsers();
        
        % Check if mobile already exists
        for i = 1:length(users)
            if strcmp(users(i).Mobile, mobile)
                errordlg('This mobile number is already registered.', 'Duplicate Error');
                return;
            end
        end
        
        % Check maximum users limit
        if length(users) >= 10
            errordlg('Maximum number of patients (10) reached.', 'Limit Error');
            return;
        end
        
        % Create new user
        newUser.Name = name;
        newUser.Mobile = mobile;
        newUser.Password = password;
        
        % Add to users array
        if isempty(users)
            users = newUser;
        else
            users(end+1) = newUser;
        end
        
        % Save users
        saveUsers(users);
        
        % Show success message
        msgbox(sprintf('Patient %s registered successfully!', name), 'Success', 'modal');
        
        % Clear fields
        clearFields();
    end

    function result = isdigit(str)
        result = all(isstrprop(str, 'digit'));
    end
end

